    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
         String cedula = btnCedula.getText();
        
        if(Supervisor.usuariosDT == null || Supervisor.usuariosDT.isEmpty()){
        String msj = "No exiten supervisor en la base de datos";
        JOptionPane.showMessageDialog(this, msj);
        }
        else{
            if(Supervisor.usuariosDT.containsKey(cedula)){
               s = Supervisor.usuariosDT.get(cedula);
               btnNombre.setText(this.s.cedula);
               btnApellido.setText(this.s.apellido);
               btnCorreo.setText(this.s.correo);
               btnDireccion.setText(this.s.direccion);
               btnNombre.setText(this.s.nombre);
               btnTelefono.setText(this.s.telefono);
               btnFecha.getEditor().setText(this.s.fecha);
               btnGenero.getSelectedItem().toString();
               btnHora.setText(this.s.horas);
               btnModificar.setEnabled(true);
               btnEliminar.setEnabled(true);
            }
            else {
                String msj = "No existe un supervisor con esa cedula " + cedula;
                JOptionPane.showMessageDialog(this, msj);
                limpiarCampos();
            }
        }
        
    }//GEN-LAST:event_btnBuscarActionPerformed

