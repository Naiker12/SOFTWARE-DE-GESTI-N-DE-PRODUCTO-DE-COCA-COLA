    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        if(btnCedula.getText() == null || btnCedula.getText().isEmpty()){
             String msj = "Para modificar primero debe buscar un supervisor";
             JOptionPane.showMessageDialog(this, msj);
            return;
        
        
        }
        if(btnCedula.getText().equals(s.cedula) != true){
            String msj = "La cdeula no coincide con la cedula del supervisor";
            JOptionPane.showMessageDialog(this, msj);
            limpiarCampos();
            return;
        }
        
        Supervisor s = Supervisor.usuariosDT.get(btnCedula.getText());
        String nombre = btnNombre.getText();
        String apellido = btnApellido.getText();
        String correo = btnCorreo.getText();
        String direccion = btnDireccion.getText();
        String telefono = btnTelefono.getText();
        String fecha = btnFecha.getEditor().getText();
        String genero = btnGenero.getSelectedItem().toString();
        String horas = btnHora.getText();
        
      
        this.s.nombre = nombre;
        this.s.apellido = apellido;
        this.s.correo = correo;
        this.s.direccion = direccion;
        this.s.telefono = telefono;
        this.s.fecha = fecha;
        this.s.genero = genero;
        this.s.horas = horas;
        
      
       Supervisor.usuariosDT.put(this.s.cedula, s);
       String msj = "Supervisor modificado con exito" ;
       JOptionPane.showMessageDialog(this, msj);
       limpiarCampos();
    }//GEN-LAST:event_btnModificarActionPerformed

