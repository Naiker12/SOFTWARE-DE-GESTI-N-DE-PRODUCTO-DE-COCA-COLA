     public boolean isEmail(String correo){
      Pattern pat = null;
      Matcher mat = null;
      pat = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
      mat = pat.matcher(correo);
      if(mat.find()){
           return true;
      }else{
           return false;
      }
     }  
