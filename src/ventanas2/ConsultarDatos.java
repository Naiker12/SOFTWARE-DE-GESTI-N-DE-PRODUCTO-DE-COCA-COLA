    private void botonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarActionPerformed
        // TODO add your handling code here:
      
        
         if(CEDULA.getText() == null || CEDULA.getText().isEmpty()){
             String msj = "Para modificar primero debe buscar un trabajador";
             JOptionPane.showMessageDialog(this, msj);
             limpiarCampos();
             return;
        }
        if(CEDULA.getText().equals(usuario.cedula) != true){
         String msj = "La cedula no coincide con cedula de trabajador";
             JOptionPane.showMessageDialog(this, msj);
             limpiarCampos();
             return;
        
        }
        this.usuario = Usuarios.usuariosBD.get(CEDULA.getText());
        String nombre = NOMBRE.getText();
        String apellido = APELLIDO.getText();
        String correo = CORREO.getText();
        String direccion = DIRECCION.getText();
        String telefono = TELEFONO.getText();
        String fecha = FECHA.getEditor() .getText();
        String hora = HORA.getText();
        
       this.usuario.nombre = nombre;
       this.usuario.apellido = apellido;
       this.usuario.correo = correo;
       this.usuario.direccion = direccion;
       this.usuario.telefono = telefono;
       this.usuario.fecha = fecha;
       this.usuario.hora = hora;
       Usuarios.usuariosBD.put(this.usuario.cedula, usuario);
       String msj = "Trabajador modificado con exito" ;
       JOptionPane.showMessageDialog(this, msj);
       limpiarCampos();
    }//GEN-LAST:event_botonModificarActionPerformed

    private void botonbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonbuscarActionPerformed
        // TODO add your handling code here:
          String cedula = CEDULA.getText();
        
        if(Usuarios.usuariosBD == null || Usuarios.usuariosBD.isEmpty()){
        String msj = "No exiten usuarios en la base de datos";
        JOptionPane.showMessageDialog(this, msj);
        }
        else{
            if(Usuarios.usuariosBD.containsKey(cedula)){
               this.usuario = Usuarios.usuariosBD.get(cedula);
               NOMBRE.setText(this.usuario.cedula);
               APELLIDO.setText(this.usuario.apellido);
               CORREO.setText(this.usuario.correo);
               DIRECCION.setText(this.usuario.direccion);
               NOMBRE.setText(this.usuario.nombre);
               TELEFONO.setText(this.usuario.telefono);
               FECHA.getEditor() .setText(this.usuario.fecha);
               HORA.setText(this.usuario.hora);
               botonModificar.setEnabled(true);
               botonEliminar.setEnabled(true);
            }
            else {
                String msj = "No existe un Trabajador con esa cedula " + cedula;
                JOptionPane.showMessageDialog(this, msj);
                limpiarCampos();
            }
        }
    }//GEN-LAST:event_botonbuscarActionPerformed

