    private void txtsuministroKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtsuministroKeyTyped
        // TODO add your handling code here:
        int key = evt.getKeyChar();

        boolean numero = key >= 48 && key <= 57;

        if (!numero){
            evt.consume();

        }

        if (txtsuministro.getText().trim().length()==10){
            evt.consume();

        }
        char validar=evt.getKeyChar();
        
        if(Character.isLetter(validar)){
           getToolkit().beep();
           evt.consume();;
           
           JOptionPane.showMessageDialog(rootPane, "Ingrese Solo Numeros");
        }
    }//GEN-LAST:event_txtsuministroKeyTyped

