    private void añadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_añadirActionPerformed
        // TODO add your handling code here:
         String  [] info =new String[6];
         info[0]=txtventas.getText();
         info[1]=txtimpuesto.getText();
         info[2]=txtsalario.getText();
         info[3]=txtalquilers.getText();
         info[4]=txtsuministro.getText();
         
         
        double total = Double.parseDouble(info [0])+Double.parseDouble(info [1])+ 
         Double.parseDouble(info [2])- Double.parseDouble(info [3])+Double.parseDouble(info [4]);
            
        info[5] = String.valueOf(total);
        
            
        modelo.addRow(info);
        

        txtventas.setText("");
        txtimpuesto.setText("");
        txtsalario.setText("");
        txtsuministro.setText("");
        txtalquilers.setText("");
       

    }//GEN-LAST:event_añadirActionPerformed

